# Fun√ß√£o para limpeza COMPLETA do ambiente
function Clear-Environment {
    Write-Host "üßπ LIMPEZA COMPLETA DO AMBIENTE..." -ForegroundColor Red
    Write-Host "-" * 50
    
    # 1. Parar TODOS os port-forwards
    Write-Host "üîå Matando TODOS os port-forwards e processos kubectl..." -ForegroundColor Cyan
    Get-Process | Where-Object { 
        $_.ProcessName -eq "kubectl" -or 
        $_.ProcessName -eq "minikube" -or
        $_.MainWindowTitle -like "*port-forward*"
    } | ForEach-Object {
        try {
            $_.Kill()
            Write-Host "‚úÖ Processo $($_.ProcessName) $($_.Id) terminado" -ForegroundColor Green
        } catch {
            Write-Host "‚ö†Ô∏è  Processo $($_.Id) j√° terminado" -ForegroundColor Yellow
        }
    }
    
    # 2. Deletar TODOS os recursos do namespace default
    Write-Host "üóëÔ∏è  Removendo TODOS os recursos do Kubernetes..." -ForegroundColor Cyan
    
    # Deletar por tipo espec√≠fico
    $resourceTypes = @(
        "hpa",
        "deployment", 
        "service",
        "configmap",
        "secret",
        "pod",
        "replicaset"
    )
    
    foreach ($type in $resourceTypes) {
        Write-Host "üóëÔ∏è  Removendo todos os $type..." -ForegroundColor Gray
        kubectl delete $type --all --ignore-not-found=true 2>$null | Out-Null
    }
    
    # 3. Aguardar tudo ser removido
    Write-Host "‚è≥ Aguardando remo√ß√£o completa..." -ForegroundColor Yellow
    Start-Sleep -Seconds 10
    
    # 4. Verificar se ainda h√° pods rodando
    $remainingPods = kubectl get pods --no-headers 2>$null
    if ($remainingPods) {
        Write-Host "ÔøΩ For√ßando remo√ß√£o de pods restantes..." -ForegroundColor Red
        kubectl delete pods --all --force --grace-period=0 2>$null | Out-Null
    }
    
    # 5. Resetar Minikube completamente
    Write-Host "üí• RESETANDO Minikube completamente..." -ForegroundColor Red
    minikube delete 2>$null | Out-Null
    
    # 6. Limpar arquivos tempor√°rios (preservar .env)
    Write-Host "üóëÔ∏è  Removendo arquivos tempor√°rios..." -ForegroundColor Cyan
    
    # Remover logs e caches (mas preservar o .env)
    $tempPaths = @("*.log", "*.tmp", ".kubectl_cache")
    foreach ($path in $tempPaths) {
        if (Test-Path $path) {
            Remove-Item $path -Recurse -Force -ErrorAction SilentlyContinue
        }
    }
    
    Write-Host ""
    Write-Host "‚úÖ LIMPEZA COMPLETA FINALIZADA!" -ForegroundColor Green
    Write-Host "üíØ Ambiente 100% limpo para nova instala√ß√£o" -ForegroundColor Green
    Write-Host ""
}

# Fun√ß√£o para verificar Minikube
function Test-MinikubeCluster {
    Write-Host "üê≥ VERIFICANDO CLUSTER MINIKUBE..." -ForegroundColor Cyan
    Write-Host "-" * 40
    
    # Verificar se Minikube est√° rodando
    $minikubeStatus = minikube status 2>$null
    if ($minikubeStatus -match "Running") {
        Write-Host "‚úÖ Minikube j√° est√° rodando" -ForegroundColor Green
        
        # Verificar metrics-server
        $metricsServer = kubectl get apiservice v1beta1.metrics.k8s.io 2>$null
        if (-not $metricsServer) {
            Write-Host "‚ö†Ô∏è  Habilitando metrics-server..." -ForegroundColor Yellow
            minikube addons enable metrics-server
        } else {
            Write-Host "‚úÖ Metrics-server dispon√≠vel" -ForegroundColor Green
        }
        
        return $true
    }
    
    Write-Host "üöÄ Iniciando Minikube..." -ForegroundColor Yellow
